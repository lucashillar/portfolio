function setLang(lang) {
  document.documentElement.lang = lang;
  document.getElementById('role').innerText = lang === 'es' ? 'Analista IT SR | Administrador CloudOps | Especialista ciberseguridad' : 'SR IT Analyst | CloudOps Administrator | Cybersecurity Specialist';
  document.querySelector('h3[data-en][data-es]').innerText = lang === 'es' ? 'Contacto' : 'Contact';
  document.querySelectorAll('b[data-en][data-es]')[0].innerText = lang === 'es' ? 'Teléfono' : 'Phone';
  document.querySelectorAll('b[data-en][data-es]')[1].innerText = lang === 'es' ? 'Ubicación' : 'Location';
  document.querySelectorAll('h3[data-en][data-es]')[1].innerText = lang === 'es' ? 'Perfil' : 'Profile';
  document.querySelectorAll('h3[data-en][data-es]')[2].innerText = lang === 'es' ? 'Experiencia' : 'Experience';
  document.querySelectorAll('h3[data-en][data-es]')[3].innerText = lang === 'es' ? 'Competencias Técnicas' : 'Technical Skills';
  document.querySelectorAll('h3[data-en][data-es]')[4].innerText = lang === 'es' ? 'Educación' : 'Education';
  document.querySelectorAll('h3[data-en][data-es]')[5].innerText = lang === 'es' ? 'Objetivos' : 'Objectives';

  const interestsH3 = document.querySelector('h3[data-en="Interests"][data-es="Intereses"]');
  if (interestsH3) interestsH3.innerText = lang === 'es' ? 'Intereses' : 'Interests';

  document.querySelector('#menu-home span').innerText = menuLabels[lang].home;
  document.querySelector('#menu-projects span').innerText = menuLabels[lang].projects;
  document.querySelector('#menu-about span').innerText = menuLabels[lang].about;
  document.querySelector('#menu-skills span').innerText = menuLabels[lang].skills;

  document.getElementById('profile-text').innerText = content[lang].profile;

  const exp = document.getElementById('experience');
  exp.innerHTML = '';
  content[lang].experience.forEach(e => {
    const div = document.createElement('div');
    div.innerHTML = e;
    exp.appendChild(div);
  });

  const skills = document.getElementById('skills');
  skills.innerHTML = '';
  content[lang].skills.forEach((s, idx) => {
    const li = document.createElement('li');
    const icon = document.createElement('i');
    icon.className = `fas ${skillsIcons[idx % skillsIcons.length]}`;
    li.appendChild(icon);
    li.appendChild(document.createTextNode(" " + s));
    skills.appendChild(li);
  });

  const edu = document.getElementById('education');
  edu.innerHTML = '';
  content[lang].education.forEach(e => {
    const li = document.createElement('li');
    li.innerText = e;
    edu.appendChild(li);
  });

  const obj = document.getElementById('objectives');
  obj.innerHTML = '';
  content[lang].objectives.forEach(o => {
    const li = document.createElement('li');
    li.innerText = o;
    obj.appendChild(li);
  });

  const loaderMsg = document.getElementById('loader-message');
  if(loaderMsg) loaderMsg.innerText = "Bienvenido/a - Welcome";
}

window.onload = function() {
  setLang('es');
};
